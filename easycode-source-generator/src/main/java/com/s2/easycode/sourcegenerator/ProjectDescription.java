package com.s2.easycode.sourcegenerator;

public class ProjectDescription {

    private String name;
    private String path;

    public void setName(final String name) {
        this.name = name;
    }

    public void setPath(final String path) {
        this.path = path;
    }

    public String getName() {
        return name;
    }

    public String getPath() {
        return path;
    }

}
